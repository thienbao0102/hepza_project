const diacriticsPattern = new RegExp(
    Object.keys(VIETNAMESE_DIACRITICS_MAP).join('|'), 'g'
);

/**
 * Remove Vietnamese diacritics from a string.
 * @param {string} str - Input string with Vietnamese diacritics
 * @returns {string} Normalized string without diacritics, lowercase
 */
export const removeDiacritics = (str) => {
    if (!str || typeof str !== 'string') return '';
    return str
        .replace(diacriticsPattern, (match) => VIETNAMESE_DIACRITICS_MAP[match] || match)
        .toLowerCase()
        .trim();
};

/**
 * Check if a search query matches a target string (diacritics-insensitive, ANY word).
 * @param {string} target - The target string to search in
 * @param {string} query - The search query
 * @returns {boolean} True if any word in query matches target
 */
export const matchesDiacriticsInsensitive = (target, query) => {
    if (!query || !target) return !query; // empty query matches everything
    const normalizedTarget = removeDiacritics(target);
    const normalizedQuery = removeDiacritics(query);
    const words = normalizedQuery.split(/\s+/).filter(Boolean);
    return words.some(word => normalizedTarget.includes(word));
};
